import turtle
t = turtle.Turtle()
t.speed(1)
t.penup()
t.goto(-100, 0)
t.pendown()
t.left(90)
t.forward(100)
t.right(90)
t.circle(-50, 180)
t.penup()
t.goto(0, 0)
t.pendown()
t.right(90)
t.forward(100)
t.right(150)
t.forward(120)
t.left(150)
t.forward(100)
t.penup()
t.goto(150,0)
t.pendown()
t.forward(100)
t.right(90)
t.forward(50)
t.backward(100)
t.hideturtle()
turtle.done()
